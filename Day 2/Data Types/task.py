# Subscripting: 문자열에서 특정 요소를 추출하는 방법
# Negative indexes 사용 가능 (-1은 문자열 마지막 문자 가져오는 것)
print("Hello"[-1])

# String
print("123" + "345")

# Integer = Whole number
print(123 + 345)

# Large Integer
# commas in the between the 1000
print(123,456,789)

# Float = Floating Point Number
# Think decimal point as being able to float around number !
print(3.14519)

# Boolean
print(True)
print(False)